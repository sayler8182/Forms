import Foundation
import UIKit

// MARK: Font
public struct Font {
    public static var awesome = Font(
        familyName: "Font Awesome 5 Free",
        fileName: "Font-Awesome")
    public static var ion = Font(
        familyName: "Ionicons",
        fileName: "Font-Ion")
    public static var material = Font(
        familyName: "Material Icons",
        fileName: "Font-Material")
    
    public let familyName: String
    public let fileName: String
    
    public func font(size: CGFloat) -> UIFont! {
        return UIFont(name: self.familyName, size: size)
    }
}

// MARK: FontError
public enum FontError: Error, LocalizedError {
    case file(Font)
    case register(Font)
     
    public var errorDescription: String? {
        switch self {
        case .file(let font): return "File not found: \(font.fileName)"
        case .register(let font): return "Can't register font: \(font.fileName)"
        }
    }
}

// MARK: Font
public extension Font {
    static func fontPaths(in bundle: Bundle) -> [String] {
        return bundle.paths(forResourcesOfType: "ttf", inDirectory: nil)
    }
    
    static func register(_ fonts: [Font],
                         in bundle: Bundle = Bundle.main) throws {
        try fonts.forEach {
            try Self.register($0, in: bundle)
        }
    }
    
    static func register(_ font: Font,
                         in bundle: Bundle = Bundle.main) throws {
        let fontName: String = font.fileName.lowercased()
        let paths: [String] = Self.fontPaths(in: bundle)
        let fontURL: URL? = paths
            .compactMap { URL(fileURLWithPath: $0) }
            .first(where: { $0.lastPathComponent.lowercased().range(of: fontName).isNotNil })
        guard let _fontURL: URL = fontURL,
            let data: Data = try? Data(contentsOf: _fontURL),
            let provider: CGDataProvider = CGDataProvider(data: data as CFData),
            let cgFont: CGFont = CGFont(provider) else { throw FontError.file(font) }
        guard CTFontManagerRegisterGraphicsFont(cgFont, nil) else { throw FontError.register(font) }
    }
    
    func register(in bundle: Bundle = Bundle.main) throws {
        try Self.register(self, in: bundle)
    }
}

